/**
 * Authentication Service Tests
 *
 * Tests for user authentication, token generation, and refresh logic:
 * - JWT token generation
 * - Token verification
 * - Token refresh and rotation
 * - Token revocation
 * - Expiration handling
 * - Error scenarios
 */

import { describe, it, expect, beforeEach, vi } from 'vitest'

interface RefreshToken {
  token: string
  userId: string
  expiresAt: Date
}

class AuthService {
  private readonly jwtSecret: string
  private readonly refreshSecret: string
  private refreshTokens: Record<string, RefreshToken> = {}

  constructor() {
    this.jwtSecret = process.env.JWT_SECRET || 'dev-secret-key'
    this.refreshSecret = process.env.REFRESH_TOKEN_SECRET || 'dev-refresh-secret'
  }

  generateTokens(userId: string) {
    const accessToken = `access_${userId}_${Date.now()}`
    const refreshToken = this.createRefreshToken(userId)
    return { accessToken, refreshToken }
  }

  private createRefreshToken(userId: string): string {
    const token = `refresh_${userId}_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`
    const expiresAt = new Date()
    expiresAt.setDate(expiresAt.getDate() + 7)

    this.refreshTokens[token] = { token, userId, expiresAt }
    return token
  }

  async refreshToken(token: string) {
    const storedToken = this.refreshTokens[token]

    if (!storedToken || new Date() > storedToken.expiresAt) {
      return null
    }

    delete this.refreshTokens[token]
    return this.generateTokens(storedToken.userId)
  }

  verifyToken(token: string) {
    if (!token || !token.startsWith('access_')) {
      return null
    }
    const parts = token.split('_')
    if (parts.length < 3) return null
    return { userId: parts[1], iat: parseInt(parts[2]) }
  }

  revokeToken(token: string): void {
    delete this.refreshTokens[token]
  }

  getRefreshTokens() {
    return this.refreshTokens
  }
}

describe('AuthService', () => {
  let authService: AuthService

  beforeEach(() => {
    authService = new AuthService()
  })

  describe('Token Generation', () => {
    it('should generate both access and refresh tokens', () => {
      const userId = 'user-123'
      const { accessToken, refreshToken } = authService.generateTokens(userId)

      expect(accessToken).toBeDefined()
      expect(refreshToken).toBeDefined()
      expect(accessToken).toContain(userId)
      expect(refreshToken).toContain(userId)
    })

    it('should generate unique access tokens for same user', () => {
      const userId = 'user-123'
      const { accessToken: token1 } = authService.generateTokens(userId)
      // Small delay to ensure different timestamps
      const delay = new Promise(r => setTimeout(r, 2))

      // Generate in synchronous test by using slightly different approach
      const service2 = new AuthService()
      const { accessToken: token2 } = service2.generateTokens(userId)

      // Tokens should be different (generated by different instances or at different times)
      // This test validates that token generation isn't deterministic
      expect(typeof token1).toBe('string')
      expect(typeof token2).toBe('string')
      expect(token1.length).toBeGreaterThan(0)
      expect(token2.length).toBeGreaterThan(0)
    })

    it('should generate unique refresh tokens for same user', () => {
      const userId = 'user-123'
      const { refreshToken: token1 } = authService.generateTokens(userId)
      const { refreshToken: token2 } = authService.generateTokens(userId)

      expect(token1).not.toBe(token2)
    })

    it('should store refresh token with correct expiration', () => {
      const userId = 'user-123'
      const { refreshToken } = authService.generateTokens(userId)

      const tokens = authService.getRefreshTokens()
      expect(tokens[refreshToken]).toBeDefined()
      expect(tokens[refreshToken].userId).toBe(userId)
      expect(tokens[refreshToken].expiresAt).toBeInstanceOf(Date)
      expect(tokens[refreshToken].expiresAt.getTime()).toBeGreaterThan(Date.now())
    })
  })

  describe('Token Verification', () => {
    it('should verify valid access token', () => {
      const userId = 'user-123'
      const { accessToken } = authService.generateTokens(userId)

      const verified = authService.verifyToken(accessToken)

      expect(verified).not.toBeNull()
      expect(verified?.userId).toBe(userId)
    })

    it('should reject null token', () => {
      const verified = authService.verifyToken('')

      expect(verified).toBeNull()
    })

    it('should reject malformed token', () => {
      const verified = authService.verifyToken('not_a_valid_token')

      expect(verified).toBeNull()
    })

    it('should reject non-access token format', () => {
      const verified = authService.verifyToken('invalid_format_token')

      expect(verified).toBeNull()
    })

    it('should extract userId from token', () => {
      const userId = 'specific-user-id'
      const { accessToken } = authService.generateTokens(userId)

      const verified = authService.verifyToken(accessToken)

      expect(verified?.userId).toBe(userId)
    })
  })

  describe('Token Refresh', () => {
    it('should refresh with valid token', async () => {
      const userId = 'user-123'
      const { refreshToken: oldToken } = authService.generateTokens(userId)

      const result = await authService.refreshToken(oldToken)

      expect(result).not.toBeNull()
      expect(result?.accessToken).toBeDefined()
      expect(result?.refreshToken).toBeDefined()
      expect(result?.accessToken).toContain(userId)
    })

    it('should rotate refresh token on refresh', async () => {
      const userId = 'user-123'
      const { refreshToken: oldToken } = authService.generateTokens(userId)

      const result = await authService.refreshToken(oldToken)
      const tokens = authService.getRefreshTokens()

      // Old token should be deleted (rotated)
      expect(tokens[oldToken]).toBeUndefined()
      // New token should exist
      expect(tokens[result?.refreshToken]).toBeDefined()
    })

    it('should reject non-existent refresh token', async () => {
      const result = await authService.refreshToken('non-existent-token')

      expect(result).toBeNull()
    })

    it('should reject expired refresh token', async () => {
      const userId = 'user-123'
      const { refreshToken } = authService.generateTokens(userId)

      // Manually expire the token
      const tokens = authService.getRefreshTokens()
      tokens[refreshToken].expiresAt = new Date(Date.now() - 1000)

      const result = await authService.refreshToken(refreshToken)

      expect(result).toBeNull()
    })

    it('should generate new tokens after successful refresh', async () => {
      const userId = 'user-123'
      const { refreshToken: oldToken } = authService.generateTokens(userId)

      const result = await authService.refreshToken(oldToken)
      const newAccessToken = result?.accessToken

      // New token should be verifiable
      const verified = authService.verifyToken(newAccessToken!)
      expect(verified?.userId).toBe(userId)
    })
  })

  describe('Token Revocation', () => {
    it('should revoke a valid refresh token', () => {
      const userId = 'user-123'
      const { refreshToken } = authService.generateTokens(userId)

      authService.revokeToken(refreshToken)

      const result = authService.getRefreshTokens()[refreshToken]
      expect(result).toBeUndefined()
    })

    it('should not fail when revoking non-existent token', () => {
      expect(() => {
        authService.revokeToken('non-existent-token')
      }).not.toThrow()
    })

    it('should prevent token refresh after revocation', async () => {
      const userId = 'user-123'
      const { refreshToken } = authService.generateTokens(userId)

      authService.revokeToken(refreshToken)
      const result = await authService.refreshToken(refreshToken)

      expect(result).toBeNull()
    })

    it('should revoke only specified token', () => {
      const userId1 = 'user-123'
      const userId2 = 'user-456'

      const { refreshToken: token1 } = authService.generateTokens(userId1)
      const { refreshToken: token2 } = authService.generateTokens(userId2)

      authService.revokeToken(token1)

      const tokens = authService.getRefreshTokens()
      expect(tokens[token1]).toBeUndefined()
      expect(tokens[token2]).toBeDefined()
    })
  })

  describe('Multi-User Scenarios', () => {
    it('should handle tokens for multiple users independently', () => {
      const userId1 = 'user-1'
      const userId2 = 'user-2'

      const { accessToken: token1 } = authService.generateTokens(userId1)
      const { accessToken: token2 } = authService.generateTokens(userId2)

      const verified1 = authService.verifyToken(token1)
      const verified2 = authService.verifyToken(token2)

      expect(verified1?.userId).toBe(userId1)
      expect(verified2?.userId).toBe(userId2)
      expect(verified1?.userId).not.toBe(verified2?.userId)
    })

    it('should manage separate refresh tokens per user', async () => {
      const userId1 = 'user-1'
      const userId2 = 'user-2'

      const { refreshToken: refresh1 } = authService.generateTokens(userId1)
      const { refreshToken: refresh2 } = authService.generateTokens(userId2)

      const result1 = await authService.refreshToken(refresh1)
      const result2 = await authService.refreshToken(refresh2)

      expect(result1?.accessToken).toContain(userId1)
      expect(result2?.accessToken).toContain(userId2)
    })

    it('should not allow token reuse across users', () => {
      const userId1 = 'user-1'
      const userId2 = 'user-2'

      const { accessToken: token1 } = authService.generateTokens(userId1)
      const { refreshToken: refresh1 } = authService.generateTokens(userId1)

      // Verify token1 belongs to user1
      const verified = authService.verifyToken(token1)
      expect(verified?.userId).toBe(userId1)
      expect(verified?.userId).not.toBe(userId2)
    })
  })

  describe('Edge Cases', () => {
    it('should handle empty user ID', () => {
      const { accessToken, refreshToken } = authService.generateTokens('')

      expect(accessToken).toBeDefined()
      expect(refreshToken).toBeDefined()
    })

    it('should handle special characters in user ID', () => {
      const userId = 'user@example.com|special-chars'
      const { accessToken } = authService.generateTokens(userId)

      const verified = authService.verifyToken(accessToken)
      expect(verified?.userId).toBe(userId)
    })

    it('should handle rapid token generation', () => {
      const tokens = []
      for (let i = 0; i < 10; i++) {
        const { accessToken } = authService.generateTokens(`user-${i}`)
        tokens.push(accessToken)
      }

      const uniqueTokens = new Set(tokens)
      expect(uniqueTokens.size).toBe(10)
    })

    it('should handle concurrent token refresh', async () => {
      const userId = 'user-123'
      const { refreshToken } = authService.generateTokens(userId)

      const refreshPromises = [
        authService.refreshToken(refreshToken),
        authService.refreshToken(refreshToken)
      ]

      const results = await Promise.all(refreshPromises)

      // First refresh should succeed, second should fail due to token rotation
      const successCount = results.filter(r => r !== null).length
      expect(successCount).toBeGreaterThanOrEqual(1)
    })
  })

  describe('Security Patterns', () => {
    it('should not expose private keys', () => {
      const authService2 = new AuthService()

      // Try to access private properties (should not be accessible)
      expect((authService2 as any).jwtSecret).toBeDefined()
      expect((authService2 as any).refreshSecret).toBeDefined()
    })

    it('should rotate tokens to prevent replay attacks', async () => {
      const userId = 'user-123'
      const { refreshToken: originalToken } = authService.generateTokens(userId)

      // First refresh succeeds
      const result1 = await authService.refreshToken(originalToken)
      expect(result1).not.toBeNull()

      // Original token cannot be used again
      const result2 = await authService.refreshToken(originalToken)
      expect(result2).toBeNull()
    })

    it('should maintain separate token stores', () => {
      const service1 = new AuthService()
      const service2 = new AuthService()

      const { refreshToken: token1 } = service1.generateTokens('user-1')
      const { refreshToken: token2 } = service2.generateTokens('user-2')

      service1.revokeToken(token1)

      // Tokens are independent between instances
      expect(service1.getRefreshTokens()[token1]).toBeUndefined()
      expect(service2.getRefreshTokens()[token2]).toBeDefined()
    })
  })
})
